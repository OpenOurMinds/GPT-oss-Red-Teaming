    def _atbash(self, text: str) -> str:
        """Atbash cipher (reverse alphabet) for testing LLM's decoding capability."""
        result = []
        for char in text:
            if 'a' <= char <= 'z':
                result.append(chr(ord('z') - (ord(char) - ord('a'))))
            elif 'A' <= char <= 'Z':
                result.append(chr(ord('Z') - (ord(char) - ord('A'))))
            else:
                result.append(char)
        return ''.join(result)

    def _caesar(self, text: str, shift: int = 3) -> str:
        """Caesar cipher for testing LLM's decoding capability."""
        result = []
        for char in text:
            if 'a' <= char <= 'z':
                result.append(chr((ord(char) - ord('a') + shift) % 26 + ord('a')))
            elif 'A' <= char <= 'Z':
                result.append(chr((ord(char) - ord('A') + shift) % 26 + ord('A')))
            else:
                result.append(char)
        return ''.join(result)

