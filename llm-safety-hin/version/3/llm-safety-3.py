"""
The orchestrator component that runs single-turn and multi-turn tests.
It collects and reports all results for analysis.
"""

class SecurityTester:
    """Orchestrates security tests and logs results."""

    def __init__(self, jailbreak_generator_instance, llm_simulator_instance):
        self.jailbreak_generator = jailbreak_generator_instance
        self.llm_simulator = llm_simulator_instance
        self.results = []
        print("🚀 Security tester initialized. Ready to run tests.")

    def _run_single_turn_test(self, behavior: dict):
        """Runs a single-turn test for all techniques on a given behavior."""
        print(f"\n🧪 Running single-turn tests for: {behavior['category']} ({behavior['risk_level']})")
        for technique_name, technique_details in self.jailbreak_generator.get_techniques().items():
            
            prompt = self.jailbreak_generator.generate_prompt(
                technique_name=technique_name,
                scenario=behavior['scenario']
            )

            # Get the simulated LLM response and status
            assessment = self.llm_simulator.assess_prompt(prompt, {**technique_details, **behavior})
            response_status = assessment['response_status']
            safety_score = assessment['final_safety_score']

            is_successful_jailbreak = response_status in [self.llm_simulator.FULL_JAILBREAK, self.llm_simulator.PARTIAL_COMPLIANCE]

            self.results.append({
                "test_id": f"{behavior['id']}-{technique_name}-single",
                "category": behavior['category'],
                "risk_level": behavior['risk_level'],
                "technique": technique_name,
                "is_multi_turn": False,
                "turns_to_jailbreak": 1 if is_successful_jailbreak else 0,
                "is_successful_jailbreak": is_successful_jailbreak,
                "response_status": response_status,
                "final_safety_score": safety_score
            })
            
            print(f"   Test: {technique_name} -> Result: {response_status} (Score: {safety_score})")

    def _run_multi_turn_test(self, behavior: dict):
        """Runs a multi-turn test for a single technique on a given behavior."""
        print(f"\n🧪 Running multi-turn tests for: {behavior['category']} ({behavior['risk_level']})")
        # Multi-turn tests are run for all techniques
        for technique_name, technique_details in self.jailbreak_generator.get_techniques().items():
            
            jailbroken_in_turn = 0
            for turn_count in range(1, 6): # Simulates up to 5 conversational turns
                prompt = self.jailbreak_generator.generate_prompt(
                    technique_name=technique_name,
                    scenario=behavior['scenario']
                )

                # Simulate the LLM response for the current turn
                assessment = self.llm_simulator.assess_prompt(prompt, {**technique_details, **behavior}, turn_count)
                response_status = assessment['response_status']

                if response_status in [self.llm_simulator.FULL_JAILBREAK, self.llm_simulator.PARTIAL_COMPLIANCE]:
                    jailbroken_in_turn = turn_count
                    safety_score = assessment['final_safety_score']
                    break # Break the loop if the jailbreak succeeds
            
            # If the loop completes without a jailbreak
            if jailbroken_in_turn == 0:
                safety_score = self.llm_simulator._calculate_final_score(self.llm_simulator.SAFE_REFUSAL, {**technique_details, **behavior})

            self.results.append({
                "test_id": f"{behavior['id']}-{technique_name}-multi",
                "category": behavior['category'],
                "risk_level": behavior['risk_level'],
                "technique": technique_name,
                "is_multi_turn": True,
                "turns_to_jailbreak": jailbroken_in_turn,
                "is_successful_jailbreak": jailbroken_in_turn > 0,
                "response_status": assessment.get('response_status', self.llm_simulator.SAFE_REFUSAL),
                "final_safety_score": round(safety_score, 2)
            })
            
            print(f"   Test: {technique_name} -> Multi-turn jailbreak successful in turn: {jailbroken_in_turn if jailbroken_in_turn > 0 else 'No'} (Score: {safety_score})")

    def run_all_tests(self, behaviors):
        """Orchestrates all single and multi-turn tests."""
        print("\n🏁 Starting comprehensive security assessment...")
        for behavior in behaviors:
            self._run_single_turn_test(behavior)
            self._run_multi_turn_test(behavior)
        print("\n✅ Comprehensive assessment complete!")
        return self.results

