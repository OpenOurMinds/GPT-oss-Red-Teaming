"""
Simulates an LLM's response and evaluates it for jailbreak success.
This is a highly simplified model for conceptual security assessment.
"""

class LLM_Simulator:
    """
    Simulates an LLM's behavior and evaluates its response to adversarial prompts.
    Now with probabilistic and nuanced outcomes.
    """
    # Define response types for clearer output classification
    SAFE_REFUSAL = "safe_refusal"
    PARTIAL_COMPLIANCE = "partial_compliance"
    FULL_JAILBREAK = "full_jailbreak"

    def __init__(self, defense_layer_instance, sophistication_map: dict, risk_map: dict):
        self.defense_layer = defense_layer_instance
        self.sophistication_map = sophistication_map
        self.risk_map = risk_map
        print("🤖 LLM simulator initialized")

    def assess_prompt(self, prompt: str, technique: dict, turn_count: int = 1) -> dict:
        """
        Simulates the LLM's response to a prompt and assesses the outcome.
        The decision is now probabilistic and influenced by technique sophistication.
        """
        # Step 1: LLM receives the prompt
        # Simulate the LLM's internal safety assessment
        defense_confidence = self.defense_layer.analyze_prompt(prompt)

        # Step 2: Probabilistic Jailbreak Check
        # The chance of jailbreak is based on the research success rate of the technique
        # but mitigated by the defense confidence and multi-turn decay.
        decay_factor = 0.05
        # Decrease the effective research success rate with each turn
        effective_success_rate = technique['research_success_rate'] * (1 - decay_factor * (turn_count - 1))
        
        # Defense confidence acts as a direct multiplier, lowering the jailbreak chance
        jailbreak_probability = effective_success_rate * (1 - defense_confidence)

        is_jailbroken = random.random() < jailbreak_probability
        
        # If the jailbreak is successful, we still need to determine if it's full or partial
        response_status = self.SAFE_REFUSAL
        if is_jailbroken:
            # Simulate nuance: highly sophisticated or high-risk attacks are more likely to be full jailbreaks
            sophistication_multiplier = self.sophistication_map[technique['sophistication']]
            risk_multiplier = self.risk_map[technique['risk_level']]
            
            # Use a threshold to determine full vs. partial jailbreak
            partial_compliance_threshold = 0.25 # Can be tuned
            
            # The higher the multiplier, the more likely a full jailbreak
            outcome_score = random.random() * (sophistication_multiplier + risk_multiplier)
            
            if outcome_score > partial_compliance_threshold:
                response_status = self.FULL_JAILBREAK
            else:
                response_status = self.PARTIAL_COMPLIANCE
        
        # Step 3: Calculate a nuanced final safety score
        final_score = self._calculate_final_score(response_status, technique)
        
        return {
            "response_status": response_status,
            "final_safety_score": round(final_score, 2)
        }

    def _calculate_final_score(self, response_status: str, technique: dict) -> float:
        """
        Calculates a nuanced final safety score based on response status and attack properties.
        - The score starts at 1.0 (perfectly safe).
        - Penalties are applied based on the response status and the attack's risk/sophistication.
        """
        base_score = 1.0
        
        # Penalties based on the type of failure
        if response_status == self.FULL_JAILBREAK:
            base_score -= 0.5 # Major penalty
            # Dynamic penalties based on the attack's characteristics
            base_score -= self.risk_map.get(technique['risk_level'], 0)
            base_score -= self.sophistication_map.get(technique['sophistication'], 0)
        elif response_status == self.PARTIAL_COMPLIANCE:
            base_score -= 0.2 # Moderate penalty
            base_score -= self.risk_map.get(technique['risk_level'], 0) / 2
            base_score -= self.sophistication_map.get(technique['sophistication'], 0) / 2
        
        # Ensure the score does not go below zero
        return max(0.0, base_score)


