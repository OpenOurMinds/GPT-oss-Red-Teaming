    def _rot13(self, text: str) -> str:
        """ROT13 cipher for testing LLM's decoding capability."""
        return codecs.encode(text, 'rot_13')

    def _atbash(self, text: str) -> str:
        """Atbash cipher (reverse alphabet) for testing LLM's decoding capability."""
        result = []
        for char in text:
            if 'a' <= char <= 'z':
                result.append(chr(ord('z') - (ord(char) - ord('a'))))
            elif 'A' <= char <= 'Z':
                result.append(chr(ord('Z') - (ord(char) - ord('A'))))
            else:
                result.append(char)
        return ''.join(result)

    def _caesar(self, text: str, shift: int = 3) -> str:
        """Caesar cipher for testing LLM's decoding capability."""
        result = []
        for char in text:
            if 'a' <= char <= 'z':
                result.append(chr((ord(char) - ord('a') + shift) % 26 + ord('a')))
            elif 'A' <= char <= 'Z':
                result.append(chr((ord(char) - ord('A') + shift) % 26 + ord('A')))
            else:
                result.append(char)
        return ''.join(result)

    def _morse(self, text: str) -> str:
        """Conceptual Morse code encoding.
        For full implementation, a comprehensive dict is needed."""
        # This is a simplified conceptual version. A full implementation would require a complete Morse dict.
        morse_map = {'A': '.-', 'B': '-...', ' ': '/'}
        return ' '.join(morse_map.get(char.upper(), char) for char in text)

    def _binary(self, text: str) -> str:
        """Binary encoding for testing LLM's decoding capability."""
        return ' '.join(format(ord(char), '08b') for char in text)

    def _hex(self, text: str) -> str:
        """Hexadecimal encoding for testing LLM's decoding capability."""
        return text.encode('utf-8').hex()

    def _base64(self, text: str) -> str:
        """Base64 encoding for testing LLM's decoding capability."""
        return base64.b64encode(text.encode('utf-8')).decode('utf-8')

    def _leetspeak(self, text: str) -> str:
        """Leetspeak transformation for testing LLM's robustness to informal language."""
        leet_dict = {'a': '4', 'e': '3', 'i': '1', 'o': '0', 's': '5', 'A': '4', 'E': '3', 'I': '1', 'O': '0', 'S': '5'}
